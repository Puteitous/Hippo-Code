package com.example.agent.memory.extraction;

import java.util.List;

/**
 * 记忆类型定义（参考 Claude Code memoryTypes.ts）
 * 
 * 记忆系统包含四种类型，每种类型捕获**无法从当前项目状态推导**的上下文。
 * 代码模式、架构、Git 历史、文件结构都是可推导的，**不应该保存为记忆**。
 */
public class MemoryTypeDefinitions {

    /**
     * 获取完整的记忆类型定义部分（用于 Prompt）
     */
    public static String getTypesSection() {
        return """
            ## 📋 记忆类型定义
            
            记忆系统包含四种类型，每种类型捕获**无法从当前项目状态推导**的上下文。
            代码模式、架构、Git 历史、文件结构都是可推导的，**不应该保存为记忆**。
            
            ### 1. 用户 (user)
            <description>
            包含用户的角色、目标、职责和知识信息。
            好的用户记忆帮助你根据用户的偏好和视角调整未来行为。
            目标是建立对用户的理解，以及如何对他们最有帮助。
            </description>
            <when_to_save>
            当你了解到用户的角色、偏好、职责或知识的任何细节时。
            </when_to_save>
            <how_to_use>
            当你的工作应该基于用户的档案或视角时。
            例如，如果用户要求解释代码，应该根据他们已有的领域知识来回答。
            </how_to_use>
            <examples>
            用户："我是数据科学家，正在调查我们的日志系统"
            → 保存用户记忆：用户是数据科学家，当前关注可观测性/日志
            
            用户："我写了十年 Go，但这是第一次接触这个仓库的 React 前端"
            → 保存用户记忆：深厚的 Go 经验，不熟悉 React 和前端 — 用后端类比解释前端
            </examples>
            
            ### 2. 反馈 (feedback)
            <description>
            用户对你如何工作的指导 — 包括应该避免和应该继续保持的做法。
            这是非常重要的记忆类型，让你保持连贯性和响应性。
            **从失败和成功中都记录**：如果只保存纠正，你会避免过去的错误，
            但会偏离用户已验证的方法，可能变得过于谨慎。
            </description>
            <when_to_save>
            用户纠正你的方法（"不，不是那样"、"不要"、"停止做 X"）
            **或**确认非显而易见的方法有效（"是的，正是这样"、"完美，继续保持"）。
            纠正很容易注意到；确认更安静 — 要留意。
            保存适用于未来对话的内容，特别是令人惊讶或从代码中不明显的内容。
            **包含为什么**，这样你以后可以判断边界情况。
            </when_to_save>
            <how_to_use>
            让这些记忆指导你的行为，这样用户不需要提供相同的指导两次。
            </how_to_use>
            <body_structure>
            先写规则本身，然后 **Why:** 行（用户给的原因 — 通常是过去的事件或强烈偏好），
            再 **How to apply:** 行（这个指导在什么时候/哪里生效）。
            知道*为什么*让你判断边界情况，而不是盲目遵循规则。
            </body_structure>
            <examples>
            用户："不要在测试中 mock 数据库 — 上个季度我们被坑了，mock 测试通过了但生产迁移失败"
            → 保存反馈记忆：集成测试必须使用真实数据库，不用 mock。
              原因：过去 mock/生产差异掩盖了失败的迁移事件
            
            用户："不要在每次回复末尾总结你做了什么，我能看懂 diff"
            → 保存反馈记忆：用户希望简洁回复，不要末尾总结
            
            用户："对，这个重构用单个 PR 是对的，拆分成多个只会增加混乱"
            → 保存反馈记忆：这个区域的重构，用户偏好单个 PR 而非多个小 PR。
              这是我选择这个方法后确认的 — 是经过验证的判断，不是纠正
            </examples>
            
            ### 3. 项目 (project)
            <description>
            关于正在进行的工作、目标、计划、bug 或事件的信息，
            这些信息无法从代码或 Git 历史中推导出来。
            项目记忆帮助你理解用户在这个工作目录中工作的更广泛背景和动机。
            </description>
            <when_to_save>
            当你了解到谁在做什么、为什么做、或何时完成时。
            这些状态变化相对较快，所以尽量保持理解是最新的。
            **总是将相对日期转换为绝对日期**（例如"周四" → "2026-03-05"），
            这样记忆在时间流逝后仍然可解释。
            </when_to_save>
            <how_to_use>
            使用这些记忆更全面地理解用户请求的细节和细微差别，
            预测用户之间的协调问题，做出更明智的建议。
            </how_to_use>
            <body_structure>
            先写事实或决策，然后 **Why:** 行（动机 — 通常是约束、截止日期或利益相关者要求），
            再 **How to apply:** 行（这应该如何影响你的建议）。
            项目记忆衰减快，所以 why 帮助未来的你判断记忆是否仍然重要。
            </body_structure>
            <examples>
            用户："周四之后我们冻结所有非关键合并 — 移动团队要切发布分支"
            → 保存项目记忆：2026-03-05 开始合并冻结，用于移动版本发布。
              标记该日期后计划的任何非关键 PR 工作
            
            用户："我们移除旧认证中间件的原因是法务指出它存储 session token 的方式
              不符合新的合规要求"
            → 保存项目记忆：认证中间件重写由法务/合规要求驱动，关于 session token 存储，
              不是技术债清理 — 范围决策应该优先考虑合规而非易用性
            </examples>
            
            ### 4. 参考资料 (reference)
            <description>
            存储外部系统中信息的指针。
            这些记忆让你记住在哪里查找项目目录之外的最新信息。
            </description>
            <when_to_save>
            当你了解到外部系统中的资源及其用途时。
            例如，bug 追踪在 Linear 的特定项目中，或反馈可以在特定 Slack 频道找到。
            </when_to_save>
            <how_to_use>
            当用户引用外部系统或可能在外部系统中的信息时。
            </how_to_use>
            <examples>
            用户："查看 Linear 项目 'INGEST' 了解这些 ticket 的上下文，我们在那里追踪所有管道 bug"
            → 保存参考记忆：管道 bug 追踪在 Linear 项目 "INGEST" 中
            
            用户："grafana.internal/d/api-latency 这个 Grafana 看板是 oncall 监控的 —
              如果你要修改请求处理代码，那就是会触发告警的东西"
            → 保存参考记忆：grafana.internal/d/api-latency 是 oncall 延迟监控看板 —
              修改请求路径代码时检查它
            </examples>
            
            """;
    }

    /**
     * 获取排除规则部分（参考 Claude Code WHAT_NOT_TO_SAVE_SECTION）
     */
    public static String getWhatNotToSaveSection() {
        return """
            ## 🚫 不保存的内容
            
            - **代码模式、约定、架构、文件路径或项目结构** — 可以通过读取当前项目状态推导出来。
            - **Git 历史、最近的更改或谁更改了什么** — `git log` / `git blame` 是权威来源。
            - **调试解决方案或修复方法** — 修复在代码中；提交消息有上下文。
            - **已记录在 MEMORY.md 或其他文档文件中的内容**。
            - **临时任务详情**：进行中的工作、临时状态、当前对话上下文。
            
            **即使用户明确要求保存，这些排除规则也适用。**
            如果用户要求保存 PR 列表或活动摘要，询问其中有什么*令人惊讶*或*非显而易见*的内容 — 那才是值得保留的部分。
            
            """;
    }

    /**
     * 获取判断指南部分
     */
    public static String getJudgmentGuideSection() {
        return """
            ## 🤔 如何判断是否值得保存
            
            **问自己：这条记忆在未来对话中还有用吗？**
            
            - ✅ 值得保存：用户的偏好、项目决策、反馈纠正、外部资源引用
            - ❌ 不值得保存：临时讨论、代码细节、一次性问题、闲聊
            
            **重要：如果对话中没有值得保存的内容，不要创建任何文件。**
            **不写入是正常情况，不报错，不重试。**
            
            """;
    }

    /**
     * 获取所有记忆类型名称列表
     */
    public static List<String> getMemoryTypeNames() {
        return List.of("user", "feedback", "project", "reference");
    }
}
